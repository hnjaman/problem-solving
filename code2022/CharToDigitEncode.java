package code2022;

import java.util.HashMap;

public class CharToDigitEncode {
    public static void main(String[] args) {
        HashMap<Character, Integer> charMap = new HashMap<>();
        charMap.put('b', 6);
        charMap.put('f', 7);
        charMap.put('g', 8);
        charMap.put('l', 1);
        charMap.put('q', 9);
        charMap.put('s', 5);
        charMap.put('w', 3);
        charMap.put('y', 4);
        charMap.put('z', 2);
    }
}
